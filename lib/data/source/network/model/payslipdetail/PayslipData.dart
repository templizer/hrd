class PayslipData {
  String absent_days;
  String absent_deduction;
  String advance_salary;
  String basic_salary;
  String company_address;
  String company_logo;
  String company_name;
  String designation;
  String employee_id;
  String employee_name;
  String fixed_allowance;
  String gross_salary;
  String holidays;
  String joining_date;
  String leave_days;
  String net_salary;
  String net_salary_figure;
  String overtime;
  String paid_leave;
  String payslip_title;
  String present_days;
  String tada;
  String tds;
  String total_days;
  String undertime;
  String employee_code;
  String unpaid_leave;
  String weekends;

  PayslipData(
      {required this.absent_days,
      required this.absent_deduction,
      required this.advance_salary,
      required this.basic_salary,
      required this.company_address,
      required this.company_logo,
      required this.company_name,
      required this.designation,
      required this.employee_id,
      required this.employee_name,
      required this.fixed_allowance,
      required this.gross_salary,
      required this.holidays,
      required this.joining_date,
      required this.leave_days,
      required this.net_salary,
      required this.net_salary_figure,
      required this.overtime,
      required this.paid_leave,
      required this.payslip_title,
      required this.present_days,
      required this.tada,
      required this.tds,
      required this.total_days,
      required this.undertime,
      required this.employee_code,
      required this.unpaid_leave,
      required this.weekends});

  factory PayslipData.fromJson(Map<String, dynamic> json) {
    return PayslipData(
      absent_days: json['absent_days'].toString(),
      absent_deduction: json['absent_deduction'].toString(),
      advance_salary: json['advance_salary'].toString(),
      basic_salary: json['basic_salary'].toString(),
      company_address: json['company_address'].toString(),
      company_logo: json['company_logo'].toString(),
      company_name: json['company_name'].toString(),
      designation: json['designation'].toString(),
      employee_id: json['employee_id'].toString(),
      employee_name: json['employee_name'].toString(),
      fixed_allowance: json['fixed_allowance'].toString(),
      gross_salary: json['gross_salary'].toString(),
      holidays: json['holidays'].toString(),
      joining_date: json['joining_date'].toString(),
      leave_days: json['leave_days'].toString(),
      net_salary: json['net_salary'].toString(),
      net_salary_figure: json['net_salary_figure'].toString(),
      overtime: json['overtime'].toString(),
      paid_leave: json['paid_leave'].toString(),
      payslip_title: json['payslip_title'].toString(),
      present_days: json['present_days'].toString(),
      tada: json['tada'].toString(),
      tds: json['tds'].toString(),
      total_days: json['total_days'].toString(),
      undertime: json['undertime'].toString(),
      employee_code: json['employee_code'].toString(),
      unpaid_leave: json['unpaid_leave'].toString(),
      weekends: json['weekends'].toString(),
    );
  }
}
